package com.temankasir.ui.pengaturan.aturoutlet.model;

public class OutletModel {

    private String idOutlet;
    private String namaOutlet;
    private String idProvinsiOutlet;
    private String namaProvinsiOutlet;
    private String idKabupatenOutlet;
    private String namaKabupatenOutlet;
    private String idKecamatanOutlet;
    private String namaKecamatanOutlet;

    public OutletModel(String idOutlet, String namaOutlet, String idProvinsiOutlet, String namaProvinsiOutlet, String idKabupatenOutlet, String namaKabupatenOutlet,
                       String idKecamatanOutlet, String namaKecamatanOutlet) {
        this.idOutlet = idOutlet;
        this.namaOutlet = namaOutlet;
        this.idProvinsiOutlet = idProvinsiOutlet;
        this.namaProvinsiOutlet = namaProvinsiOutlet;
        this.idKabupatenOutlet = idKabupatenOutlet;
        this.namaKabupatenOutlet = namaKabupatenOutlet;
        this.idKecamatanOutlet = idKecamatanOutlet;
        this.namaKecamatanOutlet = namaKecamatanOutlet;
    }

    public String getIdOutlet() {
        return idOutlet;
    }

    public void setIdOutlet(String idOutlet) {
        this.idOutlet = idOutlet;
    }

    public String getNamaOutlet() {
        return namaOutlet;
    }

    public void setNamaOutlet(String namaOutlet) {
        this.namaOutlet = namaOutlet;
    }

    public String getIdProvinsiOutlet() {
        return idProvinsiOutlet;
    }

    public void setIdProvinsiOutlet(String idProvinsiOutlet) {
        this.idProvinsiOutlet = idProvinsiOutlet;
    }

    public String getNamaProvinsiOutlet() {
        return namaProvinsiOutlet;
    }

    public void setNamaProvinsiOutlet(String namaProvinsiOutlet) {
        this.namaProvinsiOutlet = namaProvinsiOutlet;
    }

    public String getIdKabupatenOutlet() {
        return idKabupatenOutlet;
    }

    public void setIdKabupatenOutlet(String idKabupatenOutlet) {
        this.idKabupatenOutlet = idKabupatenOutlet;
    }

    public String getNamaKabupatenOutlet() {
        return namaKabupatenOutlet;
    }

    public void setNamaKabupatenOutlet(String namaKabupatenOutlet) {
        this.namaKabupatenOutlet = namaKabupatenOutlet;
    }

    public String getIdKecamatanOutlet() {
        return idKecamatanOutlet;
    }

    public void setIdKecamatanOutlet(String idKecamatanOutlet) {
        this.idKecamatanOutlet = idKecamatanOutlet;
    }

    public String getNamaKecamatanOutlet() {
        return namaKecamatanOutlet;
    }

    public void setNamaKecamatanOutlet(String namaKecamatanOutlet) {
        this.namaKecamatanOutlet = namaKecamatanOutlet;
    }
}
